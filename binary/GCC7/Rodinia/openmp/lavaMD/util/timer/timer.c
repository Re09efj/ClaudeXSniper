 // Returns the current system time in microseconds
 // gettimeofday()はvsyscall(SID)/syscallエミュレーション(Purple)経由で
 // Pin配下だと環境依存のクラッシュを起こすため、clock_gettime()に変更している。
long long get_time() {
	struct timespec ts;
	clock_gettime(CLOCK_MONOTONIC, &ts);
	return ((long long)ts.tv_sec * 1000000) + (ts.tv_nsec / 1000);
}

